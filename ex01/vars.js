// string
const fortyTwoString = "42";
console.log(`${fortyTwoString} is a ${typeof fortyTwoString}.`);

// number
const fortyTwoNumber = 42;
console.log(`${fortyTwoNumber} is a ${typeof fortyTwoNumber}.`);

// array
const fortyTwoArray = [42];
console.log(`${fortyTwoArray} is an ${typeof fortyTwoArray}.`);

// object
const fortyTwoObject = {
  fortyTwo: 42
};
console.log(`${fortyTwoObject} is an ${typeof fortyTwoObject}.`);

// boolean
const fortyTwoBoolean = true;
console.log(`${fortyTwoBoolean} is a ${typeof fortyTwoBoolean}.`);

// undefined
const fortyTwoUndefined = undefined;
console.log(`${fortyTwoUndefined} is an ${typeof fortyTwoUndefined}.`);
